/* --- REZEPTE ANZEIGEN --- */

const liste=document.getElementById("rezeptListe");

function anzeigen(filter="alle"){

if(!liste)return;

liste.innerHTML="";

rezepte
.filter(r=>filter==="alle"||r.kategorie===filter)
.forEach((rezept,index)=>{

liste.innerHTML+=`

<div class="card">
<img src="${rezept.bild}">
<h2>${rezept.titel}</h2>
<p class="kalorien">🔥 ${rezept.kalorien}</p>
<a href="rezept.html?id=${index}">Rezept ansehen →</a>
</div>

`;

});

}

anzeigen();


function filterRezepte(kategorie){
anzeigen(kategorie);
}


